nombre = "Richard"
edad = 31
diez_años = 10
cinco_años = 5
dias_del_años = 365

edad_en_10_años = edad + diez_años
print(f"{nombre} tendra en 10 años la siguiente edad: {edad_en_10_años} años")

edad_hace_cinco_años = edad - cinco_años
print(f"{nombre}, Hace 5 años tenia: {edad_hace_cinco_años} años")

dias_vividos = edad * dias_del_años
print(f"Dias vividos: {dias_vividos} dias")
